public class Operators {
    public static void main(String[] args) {
        // Operators
        // arithmetic
        var a = 1;
        var b = 5;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    // asignation
        a = b;
        System.out.println("Hello, " + a);
        a = b * 2;
        System.out.println(a);
        

    }

}
